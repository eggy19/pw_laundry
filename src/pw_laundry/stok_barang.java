    private void hapus(){
        try {
            String sql = "DELETE FROM barang WHERE kd_barang='" + txt_kode.getText() + "'";
            stat = kon.createStatement();
            stat.executeUpdate(sql);
            JOptionPane.showMessageDialog(rootPane, "Data Anda Berhasil Terubah ^-^");
            masuk_table();
            bersih();
            kd_oto();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }
    
    private void kd_oto(){
        try {
            String sql = "SELECT MAX(right(kd_barang,2)) FROM barang";
            
            stat=kon.createStatement();
            res = stat.executeQuery(sql);
            while(res.next()){
                int a = res.getInt(1);
                txt_kode.setText("B00" + Integer.toString(a+1));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }
    
    private void relasi(){
        try {
            stat = kon.createStatement();
            res = stat.executeQuery("SELECT * FROM rincian_pembelian");
            while(res.next()){
                cmb_rinci.addItem(res.getString("no_rincian"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }
    
    private void cari(){
        try {
            String sql;
            
            if(cmb_cari.getSelectedIndex()==0){
                sql = "SELECT * FROM barang";
            } else{
                String cari_ber = null;
                if(cmb_cari.getSelectedItem()=="kode barang"){
                    cari_ber = "kd_barang";
                }else if(cmb_cari.getSelectedItem()=="nama barang"){
                    cari_ber = "nm_barang";
                }
                    
                
                sql = "SELECT * FROM barang WHERE " +
                        cari_ber + " LIKE '" + "%" + txt_cari.getText() + "%" + "'";
                
                }
                stat = kon.createStatement();
                res= stat.executeQuery(sql);
                
                ResultSetMetaData meta = res.getMetaData();
                int col = meta.getColumnCount();
                int baris = 0;
                
                while(res.next()){
                    baris = res.getRow();
                }
            
                tb = new Object[baris][col];
                res.beforeFirst();
                int x =0;
                while(res.next()){
                    tb[x][0]=res.getString("kd_barang");
                    tb[x][1]=res.getString("no_rincian");   
                    tb[x][2]=res.getString("nm_barang");
                   tb[x][3]= res.getString("stok");
                    tb[x][4]=res.getString("tgl_update_stock");
                    x++;
                }
                tbl_barang.setModel(new DefaultTableModel(tb,judul));
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(rootPane, e);
        }

    }
    void angka(KeyEvent a){
        if(Character.isAlphabetic(a.getKeyChar())){
            a.consume();
            JOptionPane.showMessageDialog(rootPane, "Tidak bisa memasukan Huruf");
        }
    }
