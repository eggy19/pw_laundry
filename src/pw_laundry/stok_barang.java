     private void masuk_table(){
        try {
            stat=kon.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_UPDATABLE);
            
            tbm = new DefaultTableModel();
            tbm.addColumn("Kode Barang");
            tbm.addColumn("Rincian Pembelian");    
            tbm.addColumn("Nama Barang");            
            tbm.addColumn("stok");            
            tbm.addColumn("TGL update stok");
                               
            tbl_barang.setModel(tbm);
            
            res = stat.executeQuery("SELECT * FROM barang");
            while(res.next()){
                tbm.addRow(new Object[]{
                    res.getString("kd_barang"),
                    res.getString("no_rincian"),   
                    res.getString("nm_barang"),
                    res.getString("stok"),
                    res.getString("tgl_update_stock"),
                });
                tbl_barang.setModel(tbm);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }
    
    private void ubah(){
        try {
            String sql ="UPDATE barang SET"                    
                    + " no_rincian='"    + cmb_rinci.getSelectedItem()
                    + "', nm_barang='"    + txt_nama.getText()
                    + "', stok='"          + txt_stok.getText()
                    + "', tgl_update_stock='"   + tgl_update.getText()
                    + "' WHERE kd_barang= '"       + txt_kode.getText() + "'";
            stat = kon.createStatement();
            stat.executeUpdate(sql);
            JOptionPane.showMessageDialog(rootPane, "Data Anda Berhasil Tersimpan ^-^");
        masuk_table();
        bersih();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }
    
    private void simpan(){
        if(txt_kode.getText().equals("")
                ||txt_stok.getText().equals("")
                ||cmb_rinci.getSelectedItem().equals("")
                || tgl_update.getText().equals(""))
        {
            JOptionPane.showMessageDialog(rootPane, "Masih ada field yang kosong x_x|");
        }else
        {
           try {
           String sql = "INSERT INTO barang VALUES('"
                +txt_kode.getText()           + "','"                
                +cmb_rinci.getSelectedItem()+ "','"
                + txt_nama.getText()       +   "','"
                + txt_stok.getText()        + "','"
                +tgl_update.getText()       +   "')";
        stat = kon.createStatement();
        stat.executeUpdate(sql);
        JOptionPane.showMessageDialog(rootPane, "Data Anda Berhasil Tersimpan ^-^");
        masuk_table();
        bersih();
        kd_oto();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        } 
        }  
    }
    
